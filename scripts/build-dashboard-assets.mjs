import { execFileSync } from "node:child_process";
import { cp, mkdir, rm } from "node:fs/promises";
import { join, resolve } from "node:path";
const root = resolve(import.meta.dirname, "..");
const npmCli = process.env.npm_execpath;
const npmCommand = npmCli === undefined || npmCli.trim() === "" ? (process.platform === "win32" ? "npm.cmd" : "npm") : process.execPath;
const npmArgs = npmCli === undefined || npmCli.trim() === "" ? ["run", "build"] : [npmCli, "run", "build"];
execFileSync(npmCommand, npmArgs, { cwd: join(root, "frontend"), stdio: "inherit" });
await rm(join(root, "dist/frontend"), { recursive: true, force: true });
await mkdir(join(root, "dist/frontend"), { recursive: true });
await cp(join(root, "frontend/dist"), join(root, "dist/frontend"), { recursive: true });
